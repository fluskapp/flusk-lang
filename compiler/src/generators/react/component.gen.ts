/**
 * Component Generator - WidgetDef → reusable .tsx component files
 *
 * Generates premium React components from widget definitions.
 * Each widget type gets a typed, styled component using Tailwind CSS.
 *
 * Design system: Coral #E8384F, Blue #3B3FBF, Navy #0F1629, Warm grays
 * Font: Inter, 8pt grid spacing
 */

import type { WidgetDef, WidgetProp } from '../../parsers/widget.parser.js';

const HEADER = '// @generated by flusk-lang -- DO NOT EDIT\n';

// ── Type mapping ──────────────────────────────────────────────────────

const tsPropType = (prop: WidgetProp): string => {
  switch (prop.type) {
    case 'string': return 'string';
    case 'number': return 'number';
    case 'boolean': return 'boolean';
    case 'binding': return 'unknown';
    case 'icon': return 'string';
    case 'enum': return prop.values?.map((v) => `'${v}'`).join(' | ') ?? 'string';
    case 'string[]': return 'string[]';
    case 'object': return 'Record<string, unknown>';
    case 'object[]': return 'Record<string, unknown>[]';
    default: return 'unknown';
  }
};

// ── Component name from widget name ───────────────────────────────────

const componentName = (widgetName: string): string =>
  widgetName
    .split('-')
    .map((s) => s.charAt(0).toUpperCase() + s.slice(1))
    .join('');

// ── Generate interface ────────────────────────────────────────────────

const generateInterface = (widget: WidgetDef): string => {
  const name = componentName(widget.name);
  const lines: string[] = [`export interface ${name}Props {`];

  for (const [key, prop] of Object.entries(widget.props)) {
    const optional = prop.required ? '' : '?';
    const tsType = tsPropType(prop);
    if (prop.description) {
      lines.push(`  /** ${prop.description} */`);
    }
    // 'source' binding becomes 'data' prop (page generator passes data={data.X})
    const propName = key === 'source' && prop.type === 'binding' ? 'data' : key;
    lines.push(`  ${propName}${optional}: ${tsType};`);
  }

  // Standard props all components get
  lines.push(`  className?: string;`);
  lines.push(`  dir?: 'ltr' | 'rtl';`);
  lines.push(`}`);

  return lines.join('\n');
};

