// ── Fallback component for unknown widget types ───────────────────────

const fallbackComponent = (widget: WidgetDef): string => {
  const name = componentName(widget.name);
  const propsKeys = Object.keys(widget.props).filter((k) => k !== 'source');
  const propsStr = propsKeys.length > 0
    ? propsKeys.map((k) => `<span className="text-black/[0.30]">{${JSON.stringify(k)}}: </span><span>{String(${k} ?? '')}</span>`).join('\n          ')
    : '';

  return `
export function ${name}({ data, ${propsKeys.join(', ')}, className = '', dir }: ${name}Props) {
  return (
    <div dir={dir} className={\`rounded-xl border border-black/[0.08] bg-white p-6 \${className}\`}>
      <p className="text-xs font-medium text-black/[0.30] uppercase tracking-wider mb-2">${widget.name}</p>
      ${propsStr ? `<div className="space-y-1 text-sm text-black/[0.55]">\n          ${propsStr}\n        </div>` : '<p className="text-sm text-black/[0.02]0">No data</p>'}
    </div>
  );
}`;
};

// ── Main: generate component file ─────────────────────────────────────

export const generateComponent = (widget: WidgetDef): string => {
  const name = componentName(widget.name);
  const iface = generateInterface(widget);

  const template = COMPONENT_TEMPLATES[widget.name];
  const body = template ? template(widget) : fallbackComponent(widget);

  const lines = [
    HEADER,
    `import React from 'react';`,
    ``,
    iface,
    ``,
    body,
    ``,
  ];

  return lines.join('\n');
};

/**
 * Generate filename for component file
 */
export const componentFileName = (widget: WidgetDef): string =>
  `${componentName(widget.name)}.tsx`;

/**
 * Generate all components for the widget types used in views
 */
export const generateComponents = (
  widgetTypes: string[],
  registry: { get: (name: string) => WidgetDef | undefined },
): Array<{ path: string; content: string }> => {
  const seen = new Set<string>();
  const files: Array<{ path: string; content: string }> = [];

  for (const type of widgetTypes) {
    if (seen.has(type)) continue;
    seen.add(type);

    const def = registry.get(type);
    if (!def) continue;

    files.push({
      path: `components/${componentFileName(def)}`,
      content: generateComponent(def),
    });
  }

  return files;
};
