/**
 * Watt App Generator — produces Platformatic Watt infrastructure files
 */

import type { ParsedSchema } from '../../index.js';

const HEADER = '// @generated by flusk-lang — DO NOT EDIT\n';

const toSnake = (s: string): string =>
  s.replace(/([a-z])([A-Z])/g, '$1_$2').toLowerCase().replace(/-/g, '_');

const toCamel = (s: string): string =>
  s.replace(/-([a-z])/g, (_, c: string) => c.toUpperCase());

export interface WattFile {
  path: string;
  content: string;
}

export const generateWattConfig = (): string => {
  return JSON.stringify({
    $schema: 'https://schemas.platformatic.dev/watt/3.0.0.json',
    server: { hostname: '0.0.0.0', port: '{PLT_SERVER_PORT}' },
    autoload: { path: 'apps' },
    logger: { level: '{PLT_LOG_LEVEL}' },
    cors: { origin: '{CORS_ORIGIN}', credentials: true },
  }, null, 2) + '\n';
};

