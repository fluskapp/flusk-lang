function renderTabsLayout(section: any, indent: string, isAuth = false, ctx?: { isMarketing?: boolean }): string {
  shadcnNeeds.add('tabs');
  const tabs: any[] = section.tabs ?? [];
  if (tabs.length === 0) return `${indent}{/* Empty tabs-layout */}\n`;
  const defaultTab = tabs[0]?.id ?? 'tab-0';
  let out = `${indent}<Tabs defaultValue="${defaultTab}" className="w-full">\n${indent}  <TabsList>\n`;
  for (const tab of tabs) {
    const id = tab.id ?? tab.label?.toLowerCase().replace(/\s+/g, '-') ?? 'tab';
    const iconName = tab.icon ? lucideIcon(tab.icon) : null;
    const iconJsx = iconName ? `<${iconName} className="w-4 h-4 mr-1.5" />` : '';
    out += `${indent}    <TabsTrigger value="${id}">${iconJsx}${tab.label ?? id}</TabsTrigger>\n`;
  }
  out += `${indent}  </TabsList>\n`;
  for (const tab of tabs) {
    const id = tab.id ?? tab.label?.toLowerCase().replace(/\s+/g, '-') ?? 'tab';
    const subSections: any[] = tab.sections ?? [];
    out += `${indent}  <TabsContent value="${id}" className="space-y-6">\n`;
    for (const sub of subSections) out += renderSection(sub, indent + '    ', isAuth, ctx);
    out += `${indent}  </TabsContent>\n`;
  }
  out += `${indent}</Tabs>\n`;
  return out;
}

