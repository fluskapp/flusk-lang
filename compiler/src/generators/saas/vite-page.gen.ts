function lucideIcon(name: string): string { return ICON_MAP[name] ?? toPascal(name); }

function collectIcons(sections: any[]): Set<string> {
  const icons = new Set<string>();
  for (const section of sections) {
    for (const icon of SECTION_ICONS[section.type] ?? []) icons.add(icon);
    for (const action of section.actions ?? []) if (action.icon) icons.add(lucideIcon(action.icon));
    for (const w of section.widgets ?? []) {
      if (w.icon) icons.add(lucideIcon(w.icon));
      for (const a of w.actions ?? []) if (a.icon) icons.add(lucideIcon(a.icon));
    }
    for (const f of section.filters ?? []) {
      if (f.type === 'view-toggle') { icons.add('LayoutGrid'); icons.add('List'); }
      if (f.type === 'search') icons.add('Search');
    }
    if (section.type === 'tabs-layout') {
      for (const tab of section.tabs ?? []) {
        if (tab.icon) icons.add(lucideIcon(tab.icon));
        if (tab.sections) { const sub = collectIcons(tab.sections); for (const i of sub) icons.add(i); }
      }
    }
    // Collect icons from stat-grid stats
    for (const stat of section.stats ?? []) {
      if (stat.icon) icons.add(lucideIcon(stat.icon));
    }
  }
  return icons;
}

interface PageState { viewMode: boolean; activeTags: boolean; activeStatus: boolean; selectedFolder: boolean; collapsible: string[]; }

function analyzePageState(sections: any[]): PageState {
  const state: PageState = { viewMode: false, activeTags: false, activeStatus: false, selectedFolder: false, collapsible: [] };
  for (const section of sections) {
    if (section.type === 'filter-bar') {
      for (const f of section.filters ?? []) {
        if (f.type === 'view-toggle') state.viewMode = true;
        if (f.type === 'tag-filter') state.activeTags = true;
        if (f.type === 'status-filter') state.activeStatus = true;
      }
    }
    if (section.type === 'folder-grid') state.selectedFolder = true;
    if (section.type === 'collapsible') {
      const id = section.name?.replace(/\s+/g, '') ?? section.title?.replace(/\s+/g, '') ?? `collapse${state.collapsible.length}`;
      state.collapsible.push(id);
    }
    if (section.type === 'tabs-layout') {
      for (const tab of section.tabs ?? []) {
        for (const sub of tab.sections ?? []) {
          if (sub.type === 'collapsible') {
            const id = sub.name?.replace(/\s+/g, '') ?? sub.title?.replace(/\s+/g, '') ?? `collapse${state.collapsible.length}`;
            state.collapsible.push(id);
          }
        }
      }
    }
  }
  return state;
}

function badgeVariantExpr(statusExpr: string): string {
  return `{(() => { const s = (${statusExpr} ?? '').toLowerCase(); if (s === 'active' || s === 'connected') return 'default'; if (s === 'paused' || s === 'warning' || s === 'info') return 'secondary'; if (s === 'error' || s === 'critical') return 'destructive'; return 'outline'; })()}`;
}

function renderButton(action: any, indent: string, isAuth = false): string {
  shadcnNeeds.add('button');
  const label = action.label ?? '';
  const style = action.style ?? 'ghost';
  const iconName = action.icon ? lucideIcon(action.icon) : null;
  const iconJsx = iconName ? `<${iconName} className="w-4 h-4" />` : '';
  const rawHref = action.action ?? action.href ?? '#';
  const href = rawHref === '#' ? '#' : normalizeRoute(rawHref);
  const toastMsg = action.toast ?? null;
  let onClickBody: string;
  if (toastMsg) { shadcnNeeds.add('sonner'); onClickBody = `toast('${toastMsg}')`;
  } else if (href === '#') { shadcnNeeds.add('sonner'); onClickBody = `toast('Coming soon')`;
  } else if (isAuth) { onClickBody = `navigate('${href}')`;
  } else { onClickBody = `window.location.href = '${href}'`; }
  const onClick = ` onClick={() => { ${onClickBody}; }}`;
  const variantMap: Record<string, string> = { 'pill-dark': 'default', 'pill-outline': 'outline', 'pill-light': 'secondary', 'ghost': 'ghost', 'link': 'link', 'destructive': 'destructive' };
  const variant = variantMap[style] ?? 'ghost';
  const size = style.includes('pill') ? 'default' : 'sm';
  return `${indent}<Button variant="${variant}" size="${size}"${onClick}>${iconJsx}${label}</Button>`;
}

