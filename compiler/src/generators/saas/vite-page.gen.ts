function toKebab(name: string): string {
  return name.toLowerCase().replace(/\s+/g, '-').replace(/[^a-z0-9-]/g, '');
}

function toPascal(name: string): string {
  return name.replace(/[\s\-_]+(.)/g, (_, c: string) => c.toUpperCase()).replace(/^(.)/, (c: string) => c.toUpperCase());
}

function sourceExpr(source: string): string {
  return 'data?.' + source.replace(/\./g, '?.');
}

function normalizeRoute(href: string): string {
  return href.replace(/\/\$[a-zA-Z_]\w*\//g, '/app/').replace(/^\/\$[a-zA-Z_]\w*$/, '/app/dashboard').replace(/^\/\//, '/');
}

